package amal1package;

public class switchstatmt 
{

	public static void main(String[] args) 
	{
		int m=12;
		switch(m)
		{
		case 9:System.out.println("mon");
		break;
		
		case 10:System.out.println("tue");
		break;
		
		case 11:System.out.println("wed");
		break;
		
		case 12:System.out.println("thu");
		break;
		
		case 13: System.out.println("fri");
		break;
		
		case 14:System.out.println("sat");
		break;
		
		case 15:System.out.println("sun");
		break;
		
		default:System.out.println("invalid");
		}
				
	}

}
