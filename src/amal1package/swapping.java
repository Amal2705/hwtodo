package amal1package;

public class swapping
{

	public static void main(String[] args)
	{
		int a=10,b=20,c;
		System.out.println("before swapping value of a="+a+" before swapping value of b="+b);
		c=a;
		a=b;
		b=c;
		System.out.println("after swapping value of a="+a+" after swapping value of  b"+b);
        
	}

}
